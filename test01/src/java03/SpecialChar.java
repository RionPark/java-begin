package java03;

public class SpecialChar {

	public static void main(String[] args) {
		// char(character)는 1글자만 저장 가능
		// ' 작은따옴표로 값을 표시
		char ch = 'a';
		// String은 문자열을 저장 가능
		// " 쌍따옴표로 값을 표시
		String str = "abc";
		System.out.println("\', \"");
		System.out.println("123\t456");
		System.out.print("개행?\n");
		System.out.println("된건가?");
	}
}
